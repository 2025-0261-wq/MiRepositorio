#Escribe un programa que calcule la suma de todos los números enteros del 1 al 100 utilizando un bucle for.

print("Calculando la suma de todos los números enteros del 1 al 100...")

suma = 0
for numero in range(1, 101):
    suma += numero
print("La suma de todos los números enteros del 1 al 100 es:", suma)
